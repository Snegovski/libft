#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)

{
	char	*v;
	size_t	b;

	v = (char *)s;
	b = 0;
	while (b < n)
	{
		v[b] = c;
		b++;
	}
	return (s);
}
