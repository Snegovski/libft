int ft_strlcat( char dest, char src, int n)

{
	int c;
	int v;

	dest = 0;
	src = 0;

	c = dest;
	v = src;

	while( c != '\0')
	{
		c++;
	}
	while( v != '\0' && v < n)
	{
		c = v;
		c++;
		v++;
	}

	c = '\0';

	return(v);
}