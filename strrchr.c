#include "libft.h"

char *strchr(const char *s, int c)

{
	char *str;

	str = (char *)s;

	int v;

	v = 0;

	while(str[v] != '\0')
	{
		v++;
	}

	while(str[v] != c)
	{
		v--;
	}
	return(str+v);

}